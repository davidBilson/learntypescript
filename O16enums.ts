// ENUMS
// Enum is a way to define a set of named constants. Enums allow you to define a collection of related values that can be used interchangeable in your code

// for example, if you are building a weather app and you want to define a set of possible weather conditions, you could define an enum like this

enum WeatherConditions {
    Sunny = "Sunny",
    Cloudy = "Cloudy",
    Rainy = "Rainy",
    Snowy = "Snowy"
};

console.log(WeatherConditions.Cloudy)